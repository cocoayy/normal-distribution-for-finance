import streamlit as st


def render_notes_tab() -> None:
    """
    解説ノート用タブを表示する。
    """
    st.header("解説ノート")

    st.write(
        """
        このタブでは、このアプリが何をするものか、なぜ作ったのか、
        各数式の意味、グラフの読み方をまとめています。
        """
    )

    st.markdown("---")

    st.subheader("1. このアプリの目的")
    st.write(
        """
        このアプリは、正規分布を出発点として、

        - 数学的な理解
        - 確率の理解
        - サンプル生成による実験
        - 金融リターンとの比較
        - リスク指標の可視化
        - 時系列でのリスク変化の把握

        までを一つにつなげて理解するためのものです。
        """
    )

    st.subheader("2. 主な機能")
    st.markdown(
        """
        - **PDF / CDF 可視化**
          正規分布の形や累積確率を確認できます。

        - **区間確率**
          ある範囲に値が入る確率を確認できます。

        - **zスコア**
          値が平均から何標準偏差離れているかを確認できます。

        - **サンプル生成**
          理論分布から乱数を生成し、ヒストグラムで比較できます。

        - **金融リターン比較**
          実データの日次リターン分布と正規分布の違いを確認できます。

        - **歪度・尖度**
          分布の非対称性や裾の厚さを数値で見られます。

        - **VaR**
          一定信頼水準での損失の目安を確認できます。

        - **QQプロット**
          実データが正規分布にどれだけ近いかを確認できます。

        - **ローリングボラティリティ**
          ボラティリティが時間とともにどう変化したかを確認できます。
        """
    )

    st.markdown("---")

    st.subheader("3. 主な数式")

    st.markdown("### 3.1 正規分布の確率密度関数（PDF）")
    st.latex(r"f(x) = \frac{1}{\sqrt{2\pi}\sigma}\exp\left(-\frac{(x-\mu)^2}{2\sigma^2}\right)")
    st.write("平均 μ と標準偏差 σ を持つ正規分布の密度を表します。")

    st.markdown("### 3.2 累積分布関数（CDF）")
    st.latex(r"F(x) = P(X \le x)")
    st.write("確率変数 X が x 以下になる確率を表します。")

    st.markdown("### 3.3 区間確率")
    st.latex(r"P(a \le X \le b) = F(b) - F(a)")
    st.write("区間に入る確率は、CDF の差で求めます。")

    st.markdown("### 3.4 zスコア")
    st.latex(r"z = \frac{x - \mu}{\sigma}")
    st.write("ある値が平均から何標準偏差離れているかを示します。")

    st.markdown("### 3.5 日次リターン")
    st.latex(r"r_t = \frac{P_t}{P_{t-1}} - 1")
    st.write("前日から当日への価格変化率です。")

    st.markdown("### 3.6 ヒストリカル VaR")
    st.latex(r"\mathrm{VaR}_{\alpha} = \text{returns の } (1-\alpha)\text{ 分位点}")
    st.write("過去データの下側分位点から損失の目安を求めます。")

    st.markdown("### 3.7 パラメトリック VaR")
    st.latex(r"\mathrm{VaR}_{\alpha} = \mu + \sigma z_{1-\alpha}")
    st.write("リターンが正規分布に従うと仮定して計算します。")

    st.markdown("### 3.8 ローリングボラティリティ")
    st.latex(r"\sigma_{t,\mathrm{rolling}} = \sqrt{252}\cdot \mathrm{Std}(r_{t-w+1}, \dots, r_t)")
    st.write(
        """
        直近 w 日のリターン標準偏差を使って、時点ごとのボラティリティを計算します。
        このアプリでは日次データを年率換算するために √252 を掛けています。
        """
    )

    st.markdown("---")

    st.subheader("4. グラフの意味")

    st.markdown("### 4.1 PDF グラフ")
    st.write("正規分布の形そのものを表します。真ん中が平均で、横方向の広がりが標準偏差に対応します。")

    st.markdown("### 4.2 CDF グラフ")
    st.write("左から右へ確率を積み上げたものです。「x 以下の確率」を読むのに向いています。")

    st.markdown("### 4.3 サンプルヒストグラム")
    st.write("生成した乱数データの分布です。理論 PDF と比べることで、理論とシミュレーションの関係が見えます。")

    st.markdown("### 4.4 リターンヒストグラム")
    st.write("実際の市場リターンの分布です。正規分布を重ねることで、実データが理論からどうズレるかを見られます。")

    st.markdown("### 4.5 VaR の縦線")
    st.write("リスク閾値を示します。どこから損失側の危険領域に入るのかを視覚的に示します。")

    st.markdown("### 4.6 VaR 左尾の塗りつぶし")
    st.write("VaR よりも悪い領域、つまり損失側の危険領域を表します。")

    st.markdown("### 4.7 QQプロット")
    st.write(
        """
        実データが正規分布に近いかどうかを見るグラフです。

        - 点が直線に沿う → 正規分布に近い
        - 両端が大きくズレる → 裾が厚い
        - 曲がる → 歪みがある
        """
    )

    st.markdown("### 4.8 ローリングボラティリティ")
    st.write(
        """
        ボラティリティが時間とともにどう変化したかを見る時系列グラフです。

        - 値が高い時期 → 相場の変動が大きい
        - 値が低い時期 → 相場が比較的落ち着いている
        """
    )

    st.markdown("---")

    st.subheader("5. 歪度と尖度の意味")
    st.write(
        """
        - **歪度** は左右非対称性を表します。
        - **超過尖度** は、正規分布と比べて裾がどれくらい厚いかを表します。
        """
    )

    st.markdown("---")

    st.subheader("6. なぜ金融で重要か")
    st.write(
        """
        金融では、リターン分布を最初に正規分布で説明することが多いですが、
        実データには歪みや裾の厚さが見られ、正規分布から外れることも多いです。

        さらに、リスクは時間とともに一定ではありません。
        ローリングボラティリティを見ることで、どの時期に相場が荒れていたかを確認できます。

        このアプリは、

        - 正規分布の基礎理解
        - 実データとの比較
        - リスクの見方
        - 正規分布仮定の限界
        - 時系列でのリスク変化

        を一つのアプリで確認できるようにしています。
        """
    )
